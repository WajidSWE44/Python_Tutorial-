#Operators
#Arithmetic operators(+,-,*,/,%,**)
#Relational /Comparsion operators(==, !=, >, <, >=, <=)
#Assignment operators(=,+=,-=,*=,/=,%=,**=)
num = 10
num += 5
print("num :",num) #15

num = 10
num -= 5
print("num :",num) #5

num = 10
num /= 5
print("num :",num) #2

num = 10
num %= 5
print("num :",num) #0
 
num = 10
num *= 5
print("num :",num) #25

num = 10
num **= 5
print("num :",num) #100000

